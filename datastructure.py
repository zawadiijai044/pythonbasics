#list datastructure
#ordered
#indexed
#changeable/mutable


names=["Vanessa","Alan","Mac","Erick"]
print(names)
names[0]="Vanessa"
print(f"My name is {names[0]}")


#tuples datastructure
country=["kenya","Uganda","Asia","Dubai"]
country[2]="Uganda"
print(country)
print(f" i was born in {country[2]}")

#set datastructure
#unordered
#no index

cars=["Mazda","Subaru","Mercedes","Toyota","Ferarri"]
print(cars)

#dictionary datastructure
#key value pair
#mutable
staff={"name":"Vanessa","Age":"18","Salary":"250,000/="}
print(f"Employee name is {staff['name']}")

